package Entity;

public class Guitar {

	private int id;
	private int price;
	private Guitarspec guitarspec;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public Guitarspec getGuitarspec() {
		return guitarspec;
	}
	public void setGuitarspec(Guitarspec guitarspec) {
		this.guitarspec = guitarspec;
	}

	

}
